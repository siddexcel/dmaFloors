	<p>Aggregate exposure class denotes the surface exposure after grinding and polishing operations. The density, size and distribution of the aggregates at the surface depends on the concrete mix design and placing and finishing operations. Floor flatness at the time of grinding and polishing operations is an important consideration in selecting the appropriate aggregate exposure class.</p>
	
	<p>Surface exposure percentages are based on visual observation of the overall area of the polished floor.</p>
	
