	<h3>Overview</h3>
	<p>Wood can provide warm, timeless, enduring floors or be used as an accent for other flooring options. As a natural product wood has many benefits: renewable, carbon neutral, recyclable, and if locally sourced or reclaimed can contribute to LEED. There are over 50 species of wood commonly used in flooring today, each with unique colors, grains, hardness, and resiliency. When specifying wood in commercial spaces it is extremely important to consider many factors from the substrate to the finish. With our wood flooring specialists and extensive product library DMA Floors can help you find the right wood for your space.</p>
	<h3>Maintenance</h3>
	<p>Selecting the right floor is important, but the key to ensuring a long life out of a wood floor is a proper maintenance program. Most manufacturers can provide a detailed maintenance plan to help with this.</p>
	<h3>Walk off mats</h3>
	<p>Wood floors are susceptible to damage from chemicals and abrasives. Walk off mats on any exterior entry is highly recommended to help ease maintenance and extend the life of the floor.</p>
	<h3>Substrate</h3>
	<p>Wood can be installed over a wood or concrete sub floor, but due to its cellular construction (similar to a sponge) it is extremely susceptible to moisture.</p>
	<h4>Wood Subfloor</h4>
	<ul>
	<li>Underside of joists should be a minimum of 18” above grade</li>
	<li>Crawl space floor should be covered by 100% moisture vapor barrier (ASTM D-1745)</li>
	<li>Subfloor should be overlayed with vapor retarder with a perm rating of less than 1 when tested in accordance with ASTM E-96 Method A. Seams should overlap 4 or more inches.</li>
	<li>Impermeable membranes should not be used as they can trap moisture in the subfloor</li>
	<li>Probe moisture testing should be performed on the wood at a rate of 20 per 1000 square feet. Any readings over specification need to be investigated and addressed properly prior to installation.</li>
	</ul>
	<h4>Concrete Subfloor</h4>
	<ul>
	<li>Concrete slabs should be cured and conditioned until adequately dry.</li>
	<li>Many commonly used curing compounds will not allow the adhesive to bond to the concrete which can cause failures. It is important to either specify wet curing methods, or if a curing compound is to be used that it meet ASTM C1315 guidelines.</li>
	<li><b>Moisture testing (link)</b> should be performed using In Situ RH moisture testing in accordance with ASTM F-2170 or Calcium Chloride ASTM F1869 per manufacturer’s recommendation.,</li>
	<li>Excessive slab moisture (typically 75% or higher) will require <b>remediation (link).</b></li>
	<li>In all cases follow manufacturer’s specific installation instructions.</li>
	</ul>
	<h3>Floor Flatness (glue down over slab)</h3>
	<p>Floor flatness has typically been specified using the "straight edge" method (i.e. ¼" in 10') but this method does not address the frequency of the variance. We suggest floor flatness measurements using ASTM E1155 and ACI 302. This method addresses roll frequency, giving a better idea of the true flatness of a floor. Wood flooring manufacturers typically recommend a floor flatness of FF32-50.</p>
