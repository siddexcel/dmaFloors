					<h3>What can be done to minimize the problem?</h3>
					<ul>
						<li>Proper water/cement ratios should be a maximum of 0.42-0.5 w/c (water in excess of 0.25-0.28 is considered "water of convenience" and only assists in placement.)</li>
						<li>The pour should be monitored to prevent additional water from being added during placement.</li>
						<li>Porous aggregates should be avoided (if lightweight aggregates are needed above grade they should have a low porosity.)</li>
						<li>Hard trowelling should be avoided (this can create a thin densified layer on the surface that inhibits adequate drying.)</li>
						<li>Curing should be performed using blankets or sheeting for a minimum of 7 days (curing compounds can inhibit adhesive bond to concrete, slow drying, and may need to be mechanically removed prior to installing floorcoverings or resinous systems adding cost.)</li>
						<li>Curing compounds do not "wear off" as the concrete cures despite claims of some manufacturers.</li>
						<li>The slab should be under roof and conditioned as soon as possible</li>
					</ul>
					
