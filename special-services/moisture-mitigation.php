					 <h3>Where does moisture come from?</h3>
					 <p>Moisture comes from two principal sources; free water from the concrete mix (about a 25% ratio, by weight, of water to cement is required to hydrate the cement and cause the reaction that turns cement, aggregate and water into concrete) and from the earth beneath the slab.</p>
			</div>
			<div id="surface" class="tabcontent">
					  <h3>Why does it come to the surface?</h3>
					  <p>Before a covering is installed on the floor, the slab is trying to achieve equilibrium with the ambient air (if the ambient relative humidity is low then the slab will dry faster than if it is very humid.) After a low or impermeable flooring system is installed, the moisture in the slab will attempt to again achieve equilibrium within itself. Moisture that has settled lower in the slab will migrate towards the surface. As this moisture migrates to the surface of the slab it brings salts from the slab and creates a moist, highly alkaline environment at the interface of the flooring and the slab. This alkaline environment creates a new imbalance with the moisture in the slab and increases the level of migration in order to bring more neutral water from the slab to the area of higher alkalinity causing bond failures.</p>
			</div>
			<div id="watercement" class="tabcontent">
				<h3>Vapor Retarder, Critical Design Element?</h3>
				<p>According to the American Concrete Institute interior slabs that will have floorcoverings installed should receive a vapor retarder meeting maximum 0.1 perms (ASTM E1745) directly beneath the slab. A blotter or cushion layer of aggregate over the retarder should not be used as this creates a reservoir between the slab and vapor retarder for moisture, which can later migrate through the slab. All seams of the vapor retarder should be overlapped 6" and taped. All penetrations should be sealed watertight according to manufacturer's recommendations. Particular care should be exercised so that the membrane is not punctured during placement accidentally or intentionally (people have been known to cut holes in the membrane to accelerate the removal of bleed water and allow for faster finishing.)</p>
