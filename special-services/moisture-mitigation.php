				<h3>Make certain that the system can handle elevated pH levels.</h3>
				<p>As moisture migrates up through the slab it will increase the surface alkalinity. There are many instances where an adhesive has been placed and through moisture migration of the slab, the alkalinity increased enough to effectively destroy the adhesive. A pH tolerant system is ideal.</p>
			</div>
			<div id="CheckThickness" class="tabcontent">
				<h3>Ensure that the system has sufficient thickness to completely cover the area.</h3>
				<p>Proper preparation of the substrate should include creating an adequate surface profile so the system can achieve a strong mechanical bond. Typically proper prep is achieved through shot blasting which can create a profile as deep as 10 mils so a system that is 10 mils or less could potentially not provide full coverage over the substrate.</p>
			</div>
			<div id="PatchingLeveling" class="tabcontent">
				<h3>Does the system priced include patching and leveling the floor to accept floorcovering?</h3>
				<p>Some mitigation systems are simply that, an epoxy moisture mitigation system. This is probably adequate if you are planning on a resinous finish, but you want to make sure that the resinous system is compatible with the mitigation system. If you are planning on installing floorcovering over the mitigation system, you will need to consider patching and leveling the floor to meet the flooring manufacturer's installation guidelines.</p>
				<p><strong>What kind of warranty does the system come with?</strong> Who is involved in the warranty process? What steps need to be taken to ensure that the warranty covers your project? What is included in the warranty (some systems only warranty product replacement on the mitigation system leaving you responsible for the labor and floorcovering cost)?</p>
