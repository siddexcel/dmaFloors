				 <h3>Overview</h3>
				 <p>Despite our best efforts, we cannot control everything that can affect the drying of our slab so how do we know that when we install our floor that the slab is adequately dry? We test it. Testing does not allow for a warranty against moisture related failures, it simply minimizes the risk.</p>
				 <p>The GC has to get the job done on time, and the owner wants the job done right, early and under budget, and the flooring contractor wants to give you the best installation. Who should be responsible for moisture testing? Testing should be performed by someone who does not have a vested interest in the outcome, a third party who is an ICRI (International Concrete Repair Institute) Certified Tester. This should be outlined in the specifications for the project.</p>
			</div>
			<div id="TestingMethods" class="tabcontent">
				  <h3>Testing Methods</h3>
				  <h4>Situ Probe Relative Humidity Test (ASTM F-2170)</h4>
					<p><strong>This is the preferred method.</strong> This test uses probes placed in the slab (40% depth for slab on grade or pan, 20% depth for suspended slabs) to give a quantitative reading of the relative humidity (RH) of the slab. This method gives consistent and reliable readings of the relative humidity within the slab which gives you a better picture of what will happen once a floorcovering/resinous system is installed and the slab moisture equalizes. ASTM requires this test to be performed once the slab is in a conditioned environment. Although it will not provide an ASTM certified result, some may choose to place the probes and perform initial testing prior to conditioning to get an idea of slab moisture.</p>
					<h4>Calcium Chloride (ASTM F-1869)</h4>
					<p>This method is still specified by many manufacturers, but waning. This test will give a quantitative reading of the moisture vapor emissions at the time that the test is performed. It cannot tell you what the slab moisture content is, so it cannot predict what will happen once a system is installed. There are also frequent false readings with this test; it can be dramatically affected by ambient conditions; and it can be inconvenient in occupied spaces. This test may be used in conjunction with In Situ RH testing. This test must be performed with the slab in a conditioned environment for 48 hours prior to and during the testing process, failure to do so results in a meaningless result.</p>
					<h4>Moisture Meter (ASTM F-2659)</h4>
					<p>This method is specified by very few manufacturers. This test uses conductivity to determine moisture content near the surface of the slab. This test does not test RH at depth in the slab and like CaCl, cannot predict what will happen once flooring is installed. Conductive metals and foreign objects near the surface of the slab can affect readings.</p>
					<h4>Plastic Sheet Method (ASTM D-4263)</h4>
					<p>This method is a qualitative test and should never be used to determine whether or not the moisture content is sufficiently low to install a floorcovering or resinous system. A test resulting in a positive moisture presence will require further testing, but a negative test does not mean that the slab is at a sufficient level for installation.</p>

