					  <h3>What about admixtures that "eliminate moisture problems"?</h3>
					  <p>While there are a few manufacturers who claim that Lithium or Potassium Silicate admixtures can reduce moisture vapor emissions, there is no evidence to support these claims. These silicates are actually used as densifiers in polished concrete. One of the benefits of polished concrete is that it permits moisture vapor transmission.</p>
					  <ul>
						<li>These products can help reduce the size of capillaries within the slab and may reduce the transmission of liquid water, but will not stop vapor, which is the problem.</li>
						<li>When evaluating products ask to review the ASTM E96 test results for vapor permeability.</li>
						<li>Warranties and installation limitations should be reviewed on any mitigation system.</li>
