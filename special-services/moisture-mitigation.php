			<h3>Overview</h3>
				<p>There are a variety of mitigation systems on the market utilizing various prep methods and chemistries. When selecting a system it is important to consider why you are considering that system: solve your immediate installation problem and insure against a potential floorcovering failure. Moisture mitigation is risk mitigation and we all want the best chance of success. Epoxy type mitigation systems have consistently demonstrated superior success rates across the board and are recognized as the "right" way to get it done.</p>
			</div>
			<div id="DocumentedEvidence" class="tabcontent">
				<h3>Check that the manufacturer of the mitigation system can provide documented evidence of a proven track record of successful performance over an extended period of time.</h3>
				<p>Epoxy systems are typically installed over a properly prepared (shot blasted) floor that gives the epoxy an opportunity to penetrate and achieve maximum bonding characteristics. Epoxies also cross link as they cure which increases performance. They typically have a perm rating (ASTM E96) around .2 or less. A quality system should also have a dry film thickness around 15 mils.</p>
			</div>

			<div id="MoistureAfterInstallation" class="tabcontent">
					<h3>What does the manufacturer say about how much moisture the system can handle AFTER installation?</h3>
					<p>Moisture levels at the surface can rise once a system is installed (this is one reason why it is important to test RH and not rely on CaCl alone.) If testing was only done using CaCl you may not have a good indication of what lies beneath the surface so you want a system that will handle those increases. If there is no effective moisture vapor retarder below the slab you likewise need a system that can handle whatever comes its way.</p>
