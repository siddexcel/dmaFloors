				<h3>When is the Low Bid Not Really?</h3>
				<ul>
					<li>Floor Prep - The key to installing any epoxy system is proper floor prep. Failing to shot blast or scarify to achieve the proper profile will cause the mitigation system and the floorcovering it is supposed to protect to fail. You won't know this was done improperly until it fails!</li>
					<li>Inadequate System - There are many systems on the market that make many claims, but few are proven and provide the warranty and support to back it up.</li>
					<li>Patching/Leveling - Does the bid include patching and leveling the floor after the system is installed in order to prepare it to receive floorcovering?</li>
					<li>Patch Under Epoxy - If the floor needs to be patched prior to application of the system, is the patching material moisture tolerant? Most are not and will cause a failure.</li>
