					 <h3>Floor Flatness</h3>
					 <p>Historically we have been happy with specifying 3/16” in 10’, but what does that really tell you? Standard methods of determining floor flatness ignore the frequency of the troughs and peaks. This inconsistency can have an impact on the appearance of the floor and may increase your costs by requiring extensive patching to achieve a smooth appearance. If polishing concrete, it may make your slab unsuitable for polishing without an overlay. The American Concrete Institute recommends testing floor flatness in accordance with ASTM E1155 (link) and specifying the level of flatness required for your specified finish. There is some ambiguity as to when the flatness should be tested (some suggest testing as soon as you can walk on the floor,) but testing will provide the most accurate readings after the floor has cured. Waiting until cure allows you to account for slab curl during curing, which will affect finishes.</p>
			</div>
			<div id="ExistingConditions" class="tabcontent">
					  <h3>Existing Conditions</h3>
					  <p>What is on the floor (sealers, glues, mortars, epoxies, etc.) can have a monumental financial impact on the project if proper procedures are not utilized to abate the current conditions. While some of these can be easily removed, some may require shaving the slab and removal of significant material. Whether you need to remove ¼” of epoxy or simply a cure and seal there is still a financial impact on the project that needs to be considered and specified. It is important as well to know what may lie under current finishes that are to be removed. We worked with a general contractor on a small project that required a polished concrete finish, but as they removed the carpet they discovered that the floor was well beyond any conditions that could be polished and would require an overlay.</p>
			</div>
			<div id="Moisture" class="tabcontent">
					  <h3>Moisture</h3>
					  <p>There are two types of moisture issues that can affect your floor; fluid and moisture vapor. Fluid is less common and typically found only when the slab lies below the surrounding water table or during flooding. Moisture vapor emission (MVE) is the most common and has become more of an issue with fast track construction schedules and changes in adhesives over the past decade. Excessive MVE can be caused by lack of cure time under controlled conditions and excessive “water of convenience” used during the placement of the slab (this is why it is important to specify and monitor the proper water/cement ratio for your needs - ACI 211.1,) as well as improper, damaged, or non-existent vapor retarders (ASTM E1745-11.) There are options to remediate MVE (link to moisture remediation) as well as finishes that are not sensitive to moisture vapor emissions (some resinous systems, ceramic tile and polished concrete.)</p>
