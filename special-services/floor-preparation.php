function openCustomTab(evt, tabName, parentID) {
    var i, tabcontent, tablinks;
    var parentElement = document.getElementById(parentID);
	
	console.log({parentElement});

    // Hide all tab contents within the specified parent element
    tabcontent = parentElement.getElementsByClassName("tabcontent");
    for (i = 0; i < tabcontent.length; i++) {
        tabcontent[i].style.display = "none";
    }

    // Remove 'active' class from all tab links within the specified parent element
    tablinks = parentElement.getElementsByClassName("tablinks");
    for (i = 0; i < tablinks.length; i++) {
        tablinks[i].className = tablinks[i].className.replace(" active", "");
    }

    // Display the current tab content and mark the button as active
    document.getElementById(tabName).style.display = "block";
    evt.currentTarget.className += " active";
}

