				 <h3>Control Joints</h3>
				 <p>Control joints are important to slab design, but must be addressed when installing floor coverings or resinous systems. Some people choose to leave them untreated when installing polished concrete or in warehouse areas, but treating them with a semi-rigid resinous filler can help appearances, eliminate dirt and build up as well as help the slab hold up better under heavy wheeled traffic. Any cracking that occurs in the floor can be telegraphed through most hard surface and resinous finishes if not properly addressed. Joints in Concrete Construction – ACI 224.3R-22</p>
			</div>
			<div id="ExpansionJoints" class="tabcontent">
				  <h3>Expansion Joints</h3>
				  <p>Similar to control joints, expansion joints need to be addressed properly, refer to Joints in Concrete Construction – ACI 224.3R-22. Expansion joints should never be covered. Damaged expansion joint edges can be repaired and joints should be filled using flexible fill material, proper backer rod and elastomeric joint sealer filled to a depth of 2/3 the width of the joint.</p>
			</div>
			<div id="SurfaceProfile" class="tabcontent">
				  <h3>Surface Profile</h3>
				  <p>When installing overlays, some adhesives, moisture mitigation systems, and resinous systems it is essential to have a proper surface profile. There are typically four proper methods of creating a profile; milling, scarifying, grinding, and shot blasting. Acid etching is sometimes used for thin mil resinous systems, but should be avoided (it is often done incorrectly;it can be hazardous; it is ineffective if there is a sealer/curing agent on the floor.) Mechanical methods provide a consistently better profile. Most profiles are specified by the manufacturer of the product to be installed and are described by the International Concrete Repair Institute guide 03732 and graded from CSP1-9. The CSP level and approved methods of achieving the profile should be specified independently as some manufacturers still list acid etching as an approved method (to appeal to lesser equipped installers.) See also ASTM D4259-88. Surface profiling is one of the easiest places for a contractor to cut corners because no one will see it until the floor fails. DMA can help you determine the proper CSP for your specified system and show you what the profile should look like.</p>
